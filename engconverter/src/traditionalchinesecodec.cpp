TraditionalChineseCodec::TraditionalChineseCodec(Logger &logger) : QTextCodec(), logger(logger)
{
    for (int i = 0; i < MAX_CHARS; i++) {
        int internal = entries[i].internal;
        QChar unicode = entries[i].unicode;
        internalToUnicode[internal] = unicode;
        unicodeToInternal[unicode] = internal;
    }
    for (int i = 0; big5Entries[i].internal; i++) {
        internalToUnicode[big5Entries[i].internal] = big5Entries[i].unicode;
    }
}

