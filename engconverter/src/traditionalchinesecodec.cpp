QString TraditionalChineseCodec::convertToUnicode(const char *chars, int len, ConverterState *state) const
{
    QString result = QString();
    result.reserve(len);
    int index = 0;
    int invalid = 0;
    while (index < len) {
        if ((chars[index] & 0x80) == 0) {
            // ascii character, copy as-is
            result += QChar((ushort)chars[index]);
            index++;
        } else if (index + 1 < len) {
            // 2-byte char, get value
            int id = (chars[index] & 0xff) | ((chars[index + 1] << 8) & 0xff00);
            QChar c = internalToUnicode.value(id);
            if (c != 0) {
                result += c;
            } else {
                logger.warn(QString("Invalid character: %1").arg(id));
                invalid++;
            }
            index += 2;
        } else {
            break;
        }
    }
    if (state) {
        state->remainingChars = len - index;
        state->invalidChars += invalid;
    }
    return result;
}

QByteArray TraditionalChineseCodec::convertFromUnicode(const QChar *chars, int len, ConverterState *state) const
{
    QByteArray bytes = QByteArray();
    bytes.reserve(2 * len);
    int index = 0;
    int invalid = 0;
    while (index < len) {
        QChar c = chars[index];
        if (c.unicode() < 0x80) {
            bytes += (char) (c.unicode() & 0xff);
        } else {
            int internal = unicodeToInternal.value(c);
            if (internal) {
                bytes += (char) (internal & 0xff);
                bytes += (char) ((internal >> 8) & 0xff);
            } else {
                logger.warn(QString("Unsupported character: %1").arg(c));
            }
        }
        index++;
    }
    if (state) {
        state->remainingChars = len - index;
        state->invalidChars += invalid;
    }
    return bytes;
}
